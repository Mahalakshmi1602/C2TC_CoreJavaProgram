package dayone;

public class CharDemo {

	public static void main(String[] args) {
		
		char ch = 'a';
		System.out.println(ch);

		
		char ch1 = 16;
		System.out.println(ch1);

		
		char var1 = '\u00A9';
		System.out.println(var1);

		
		char var2 = '\u16AC';
		System.out.println(var2);

		
		int a = 'M';
		System.out.println(a);
	}
}
